import java.lang.annotation.Target;

@Retention(RetentionPolicy.RUNTIME)
@Target({ ElementType.PARAMETER, ElementType.METHOD })
/** a type that is hidden by type erasure */
public @interface HiddenTypes2 {
    Class<?> class1();
    Class<?> class2();
}
